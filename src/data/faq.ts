const pickFaqByQuestions = (questions: readonly string[]): FaqItem[] =>
  questions.map((q) => {
    const item = faqData.find((f) => f.q === q);
    if (!item) throw new Error(`FAQ item not found: ${q}`);
    return item;
  });

